import { gzipSync } from "node:zlib";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const projectDir = resolve(scriptDir, "..");
const repoDir = resolve(projectDir, "..");
const distDir = resolve(projectDir, "dist-web");
const outFile = resolve(repoDir, "components", "radar_api_server", "dashboard_assets.h");
const versionFile = resolve(repoDir, "version.json");

const assets = [
  {
    name: "INDEX_HTML",
    path: resolve(distDir, "index.html"),
    route: "/dashboard",
    contentType: "text/html; charset=utf-8"
  },
  {
    name: "DASHBOARD_JS",
    path: resolve(distDir, "assets", "dashboard.js"),
    route: "/dashboard/assets/dashboard.js",
    contentType: "text/javascript; charset=utf-8"
  },
  {
    name: "DASHBOARD_CSS",
    path: resolve(distDir, "assets", "dashboard.css"),
    route: "/dashboard/assets/dashboard.css",
    contentType: "text/css; charset=utf-8"
  }
];

function formatBytes(bytes) {
  return Array.from(bytes, (byte, index) => {
    const suffix = index % 16 === 15 ? "\n" : " ";
    return `0x${byte.toString(16).padStart(2, "0")},${suffix}`;
  }).join("");
}

const generated = assets.map((asset) => {
  const raw = readFileSync(asset.path);
  const gzip = gzipSync(raw, { level: 9 });
  return {
    ...asset,
    rawSize: raw.length,
    gzip,
    gzipSize: gzip.length
  };
});

const totalGzipSize = generated.reduce((sum, asset) => sum + asset.gzipSize, 0);
const version = JSON.parse(readFileSync(versionFile, "utf8"));
const firmwareVersion = String(version.firmware ?? "0.0.0");
const dashboardVersion = String(version.dashboard ?? "0.0.0");
const configSchemaVersion = Number(version.configSchema ?? 1);
const floorplanSchemaVersion = Number(version.floorplanSchema ?? 1);
const statsSchemaVersion = Number(version.statsSchema ?? 1);

const body = `// This file is generated by dashboard/scripts/generate-dashboard-assets.mjs.
// Do not edit manually.
#pragma once

#include <cstddef>
#include <cstdint>

namespace esphome {
namespace radar_api_server {
namespace dashboard_assets {

inline constexpr const char *FIRMWARE_VERSION = "${firmwareVersion}";
inline constexpr const char *DASHBOARD_VERSION = "${dashboardVersion}";
inline constexpr int CONFIG_SCHEMA_VERSION = ${configSchemaVersion};
inline constexpr int FLOORPLAN_SCHEMA_VERSION = ${floorplanSchemaVersion};
inline constexpr int STATS_SCHEMA_VERSION = ${statsSchemaVersion};

${generated
  .map(
    (asset) => `inline constexpr const char *${asset.name}_ROUTE = "${asset.route}";
inline constexpr const char *${asset.name}_CONTENT_TYPE = "${asset.contentType}";
inline constexpr std::size_t ${asset.name}_RAW_SIZE = ${asset.rawSize};
inline constexpr std::uint8_t ${asset.name}_GZ[] = {
${formatBytes(asset.gzip)}};
inline constexpr std::size_t ${asset.name}_GZ_SIZE = sizeof(${asset.name}_GZ);
`
  )
  .join("\n")}
inline constexpr std::size_t DASHBOARD_TOTAL_GZ_SIZE = ${totalGzipSize};

}  // namespace dashboard_assets
}  // namespace radar_api_server
}  // namespace esphome
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, body);

for (const asset of generated) {
  console.log(`${asset.route}: ${asset.rawSize} bytes -> ${asset.gzipSize} gzip bytes`);
}
console.log(`dashboard gzip total: ${totalGzipSize} bytes`);
