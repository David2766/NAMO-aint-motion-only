function readJson(relativePath) {
  return JSON.parse(readText(relativePath));
}

function readText(relativePath) {
  const path = resolve(repoRoot, relativePath);
  if (!existsSync(path)) fail(`required source file not found: ${relativePath}`);
  return readFileSync(path, "utf8").replaceAll("\r\n", "\n").trimEnd();
}

function readStringConstant(source, name) {
  const match = source.match(new RegExp(`const\\s+${name}\\s*=\\s*["']([^"']+)["']`));
  return match?.[1] ?? "";
}

function validateBuiltFirmwareDependencies() {
  const buildRoot = resolve(repoRoot, ".esphome", "build", "presence-sensor");
  const dependencyLockPath = resolve(buildRoot, "dependencies.lock");
  if (!existsSync(dependencyLockPath)) return;

  const dependencyLock = readFileSync(dependencyLockPath, "utf8").replaceAll("\r\n", "\n");
  const idfVersion = readDependencyVersion(dependencyLock, "idf");
  if (idfVersion !== metadata.espIdf.version) {
    fail(`ESP-IDF build version ${idfVersion || "unknown"} does not match notice metadata ${metadata.espIdf.version}`);
  }

  for (const component of metadata.managedFirmwareComponents) {
    const actualVersion = readDependencyVersion(dependencyLock, component.id);
    if (actualVersion !== component.version) {
      fail(`${component.name} build version ${actualVersion || "unknown"} does not match notice metadata ${component.version}`);
    }

    const builtLicensePath = resolve(
      buildRoot,
      "managed_components",
      component.buildDirectory,
      component.licenseFile
    );
    if (!existsSync(builtLicensePath)) {
      fail(`built license file not found for ${component.name}: ${builtLicensePath}`);
    }
    const builtLicense = readFileSync(builtLicensePath, "utf8").replaceAll("\r\n", "\n").trimEnd();
    const storedLicense = readText(component.licenseSource);
    if (builtLicense !== storedLicense) {
      fail(`${component.name} license text changed; refresh ${component.licenseSource}`);
    }
  }
}

function readDependencyVersion(lockText, id) {
  const escapedId = id.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const block = lockText.match(new RegExp(`^  ${escapedId}:\\n([\\s\\S]*?)(?=^  [^ \\n]+:|^direct_dependencies:)`, "m"));
  return block?.[1].match(/^    version:\s*['"]?([^'"\s]+)['"]?\s*$/m)?.[1] ?? "";
}

function section(title, body) {
  return `${"=".repeat(78)}\n${title}\n${"=".repeat(78)}\n\n${body}\n`;
}

function writeOrCheck(relativePath, content) {
  const path = resolve(repoRoot, relativePath);
  const normalized = `${content.trimEnd()}\n`;
  if (checkOnly) {
    const current = existsSync(path)
      ? readFileSync(path, "utf8").replaceAll("\r\n", "\n")
      : "";
    if (current !== normalized) {
      fail(`${relativePath} is out of date; run node scripts/generate-third-party-notices.mjs`);
    }
    return;
  }
  writeFileSync(path, normalized, "utf8");
  console.log(`generate-third-party-notices: wrote ${relativePath}`);
}

function fail(message) {
  console.error(`generate-third-party-notices: ${message}`);
  process.exit(1);
}
